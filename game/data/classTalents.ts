const createNode = ({
  id,
  classId,
  trailId,
  trailName,
  title,
  description,
  tier,
  color,
  icon,
  prerequisites = [],
  effects,
}: Omit<TalentNode, 'cost' | 'requiredLevel' | 'prerequisites'> & {
  prerequisites?: string[];
}) => ({
  id,
  classId,
  trailId,
  trailName,
  title,
  description,
  tier,
  cost: Math.max(1, Math.min(3, tier)),
  requiredLevel: tier * 2 - 1,
  color,
  icon,
  prerequisites,
  effects,
});

